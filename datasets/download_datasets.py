"""
Sentinex AI Security Scanner - Dataset Downloader
Downloads weapon/threat-related classes from Google Open Images V7
using FiftyOne and exports to YOLO format for training.

Google Open Images V7: 1.7M+ images, 600 classes, completely free.
"""

import os
import sys
import argparse
from pathlib import Path


def download_open_images(
    output_dir: str = "datasets/open_images_weapons",
    max_samples_per_split: dict = None,
    classes: list = None,
):
    """
    Download weapon-related classes from Google Open Images V7
    and export to YOLOv5/v8 format.
    
    Args:
        output_dir: Directory to export the YOLO-formatted dataset
        max_samples_per_split: Dict of {split: max_samples} e.g. {"train": 5000, "validation": 1000}
        classes: List of Open Images class names to download
    """
    try:
        import fiftyone as fo
        import fiftyone.zoo as foz
    except ImportError:
        print("=" * 60)
        print("ERROR: FiftyOne is not installed.")
        print("Install it with: pip install fiftyone")
        print("=" * 60)
        sys.exit(1)

    # Default weapon/threat classes available in Open Images V7
    if classes is None:
        classes = [
            "Knife",
            "Handgun",
            "Dagger",
            "Scissors",
            "Sword",
        ]

    if max_samples_per_split is None:
        max_samples_per_split = {
            "train": 5000,
            "validation": 1000,
            "test": 500,
        }

    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    print("=" * 60)
    print("SENTINEX AI - Google Open Images V7 Downloader")
    print("=" * 60)
    print(f"Target classes: {classes}")
    print(f"Output directory: {output_path.resolve()}")
    print()

    # Download and export each split
    for split, max_samples in max_samples_per_split.items():
        print(f"\n{'─' * 50}")
        print(f"Downloading '{split}' split (max {max_samples} samples)...")
        print(f"{'─' * 50}")

        dataset_name = f"sentinex-oiv7-{split}"

        # Delete existing dataset with same name if it exists
        if dataset_name in fo.list_datasets():
            fo.delete_dataset(dataset_name)

        dataset = foz.load_zoo_dataset(
            "open-images-v7",
            split=split,
            label_types=["detections"],
            classes=classes,
            max_samples=max_samples,
            dataset_name=dataset_name,
        )

        # Map split names for YOLO directory structure
        yolo_split = "val" if split == "validation" else split
        split_export_dir = str(output_path / yolo_split)

        print(f"Exporting {len(dataset)} samples to YOLO format -> {split_export_dir}")

        dataset.export(
            export_dir=split_export_dir,
            dataset_type=fo.types.YOLOv5Dataset,
            label_field="ground_truth",
            classes=classes,
        )

        # Clean up FiftyOne dataset from DB (keep files)
        fo.delete_dataset(dataset_name)

        print(f"✓ '{yolo_split}' split exported successfully ({len(dataset)} images)")

    # Generate data.yaml for YOLO training
    _generate_data_yaml(output_path, classes)

    print("\n" + "=" * 60)
    print("DOWNLOAD COMPLETE!")
    print("=" * 60)
    print(f"Dataset location: {output_path.resolve()}")
    print(f"data.yaml: {(output_path / 'data.yaml').resolve()}")
    print(f"\nTo train: python training/train_yolo.py")
    print("=" * 60)


def _generate_data_yaml(output_path: Path, classes: list):
    """Generate YOLO-compatible data.yaml file."""
    yaml_content = f"""# Sentinex AI Security Scanner - Dataset Configuration
# Source: Google Open Images V7
# Auto-generated by download_datasets.py

path: {output_path.resolve()}
train: train/images
val: val/images
test: test/images

# Number of classes
nc: {len(classes)}

# Class names
names:
"""
    for i, cls in enumerate(classes):
        yaml_content += f"  {i}: {cls.lower()}\n"

    yaml_path = output_path / "data.yaml"
    yaml_path.write_text(yaml_content)
    print(f"\n✓ Generated data.yaml with {len(classes)} classes")


def download_coco_subset():
    """
    Fallback: Download COCO128 (built into Ultralytics) for quick testing.
    No extra dependencies needed.
    """
    print("=" * 60)
    print("SENTINEX AI - COCO Fallback Dataset")
    print("=" * 60)
    print("Using Ultralytics built-in COCO128 for quick testing.")
    print("For best results, use --source openimages instead.")
    print()

    try:
        from ultralytics import YOLO
        model = YOLO("yolov8n.pt")
        # This triggers COCO128 auto-download
        model.val(data="coco128.yaml", imgsz=640)
        print("\n✓ COCO128 dataset ready at datasets/coco128")
    except Exception as e:
        print(f"Error: {e}")
        sys.exit(1)


def main():
    parser = argparse.ArgumentParser(
        description="Sentinex AI - Dataset Downloader",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Download from Google Open Images V7 (recommended)
  python datasets/download_datasets.py --source openimages

  # Download with custom sample limits
  python datasets/download_datasets.py --source openimages --train-samples 10000 --val-samples 2000

  # Quick test with COCO128 (no extra deps)
  python datasets/download_datasets.py --source coco

  # Download specific classes only
  python datasets/download_datasets.py --source openimages --classes Knife Handgun
        """,
    )

    parser.add_argument(
        "--source",
        choices=["openimages", "coco"],
        default="openimages",
        help="Dataset source (default: openimages)",
    )
    parser.add_argument(
        "--output-dir",
        default="datasets/open_images_weapons",
        help="Output directory for dataset (default: datasets/open_images_weapons)",
    )
    parser.add_argument(
        "--train-samples",
        type=int,
        default=5000,
        help="Max training samples to download (default: 5000)",
    )
    parser.add_argument(
        "--val-samples",
        type=int,
        default=1000,
        help="Max validation samples to download (default: 1000)",
    )
    parser.add_argument(
        "--test-samples",
        type=int,
        default=500,
        help="Max test samples to download (default: 500)",
    )
    parser.add_argument(
        "--classes",
        nargs="+",
        default=None,
        help="Specific classes to download (default: Knife Handgun Dagger Scissors Sword)",
    )

    args = parser.parse_args()

    if args.source == "openimages":
        download_open_images(
            output_dir=args.output_dir,
            max_samples_per_split={
                "train": args.train_samples,
                "validation": args.val_samples,
                "test": args.test_samples,
            },
            classes=args.classes,
        )
    elif args.source == "coco":
        download_coco_subset()


if __name__ == "__main__":
    main()
